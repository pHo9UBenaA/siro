/**
 * Pure transforms that scaffold a new rule into the codebase. The shell driver
 * `scripts/gen/rule.mjs` handles argv parsing, FS I/O, and running
 * gen:rules / gen:comparison — this module is only string-in / string-out so
 * it can be unit-tested without touching disk.
 */

export type RuleType = 'auto' | 'advisory';

const RULE_ID_PATTERN = /^[a-z][a-z0-9]*(?:-[a-z0-9]+)*$/u;

export const isValidRuleId = (id: string): boolean => RULE_ID_PATTERN.test(id);

export const kebabToCamel = (id: string): string =>
  id.replaceAll(/-(?<char>[a-z0-9])/gu, (_match, ch: string) => ch.toUpperCase());

const renderAutoRule = (id: string, camelId: string): string =>
  `import { requireConfigKey } from './builders/require-config-key.ts';

// Stub generated by \`pnpm gen:rule\`.
// Fill bindings, severity, description, and docs URL. See
// docs/contributing.md → "Adding a new rule" for the guide and
// src/domain/rules/frozen-lockfile.ts for a worked example.
export const ${camelId} = requireConfigKey({
  id: '${id}',
  title: 'TODO: short title',
  description: 'TODO: one-sentence rule description',
  severity: 'warn',
  docs: 'TODO: upstream docs URL',
  // TODO(siro): bindings are empty; the rule will match no PM and ship as a
  // permanent no-op until at least one binding is filled in.
  bindings: {},
});
`;

const renderAdvisoryRule = (id: string, camelId: string): string =>
  `import type { Rule } from '../entities/rule.ts';

// Stub generated by \`pnpm gen:rule --advisory\`.
// Advisory bindings carry their own \`check\` and \`fix\` functions per PM. See
// docs/contributing.md → "Adding a new rule" for the guide and
// src/domain/rules/files-field.ts for a worked example.
export const ${camelId}: Rule = {
  id: '${id}',
  title: 'TODO: short title',
  description: 'TODO: one-sentence rule description',
  severity: 'info',
  docs: 'TODO: upstream docs URL',
  // TODO(siro): bindings are empty; the rule will match no PM and ship as a
  // permanent no-op until at least one binding is filled in.
  bindings: {},
};
`;

export const renderRuleFile = (id: string, camelId: string, type: RuleType): string => {
  if (type === 'advisory') {
    return renderAdvisoryRule(id, camelId);
  }
  return renderAutoRule(id, camelId);
};

const RULE_ID_ARRAY_OPEN = 'export const BUILTIN_RULE_IDS = [';
const RULE_ID_ARRAY_CLOSE = '] as const;';
const RULE_REGISTRY_OPEN = 'const RULE_REGISTRY = {';
const RULE_REGISTRY_CLOSE = '} as const satisfies Record<BuiltinRuleId, Rule>;';

const DEFAULT_SEARCH_START = 0;
const NEXT_LINE = 1;
const NOT_FOUND = -1;
const INSERT_COUNT = 0;

interface FindMarkerLineOptions {
  readonly lines: readonly string[];
  readonly marker: string;
  readonly what: string;
  readonly from?: number;
}

/**
 * Locate the line whose trimmed content equals `marker`. Exact trimmed-match
 * (instead of `startsWith`/`includes`) rules out two failure classes at once:
 * a commented-out occurrence on a JSDoc line cannot anchor the splice, AND a
 * future top-level array elsewhere in the file (with the same `];` close)
 * cannot steal the match from the canonical array. The trade-off is that any
 * formatter / hand-edit that changes the line's exact shape now fails this
 * lookup with a clear error rather than splicing into the wrong location.
 */
const findMarkerLine = (opts: FindMarkerLineOptions): number => {
  const { lines, marker, what, from = DEFAULT_SEARCH_START } = opts;
  for (let idx = from; idx < lines.length; idx += NEXT_LINE) {
    const line = lines[idx];
    if (typeof line !== 'undefined' && line.trim() === marker) {
      return idx;
    }
  }
  throw new Error(
    `Marker for ${what} not found: expected a line whose trimmed content is exactly \`${marker}\`. ` +
      `If you renamed or reformatted the source, sync scripts/gen/lib/rule-scaffolder.ts.`,
  );
};

// BUILTIN_RULE_IDS is in insertion order (roughly severity-impact grouping),
// not alphabetical — so this appends at the end. The compile-time check in
// builtin-rules.ts uses `Record<BuiltinRuleId, Rule>` (key-set equivalence),
// so insertion order between this tuple and the registry can diverge freely.
export const insertRuleIdEntry = (source: string, id: string): string => {
  const lines = source.split('\n');
  const startIdx = findMarkerLine({
    lines,
    marker: RULE_ID_ARRAY_OPEN,
    what: 'BUILTIN_RULE_IDS opening',
  });
  const endIdx = findMarkerLine({
    from: startIdx + NEXT_LINE,
    lines,
    marker: RULE_ID_ARRAY_CLOSE,
    what: 'BUILTIN_RULE_IDS closing',
  });
  const body = lines.slice(startIdx, endIdx).join('\n');
  if (body.includes(`'${id}'`)) {
    throw new Error(`rule id '${id}' already present in BUILTIN_RULE_IDS`);
  }
  lines.splice(endIdx, INSERT_COUNT, `  '${id}',`);
  return lines.join('\n');
};

const naturalOrder = (left: string, right: string): number =>
  left.localeCompare(right, void 0, { numeric: true });

const RULES_IMPORT_RE =
  /^import\s+\{\s*\w+\s*\}\s+from\s+'\.\/rules\/(?<ruleId>[a-z0-9-]+)\.ts';$/u;

interface ImportScanState {
  insertImportAt: number;
  lastRulesImportIdx: number;
}

const AFTER_CURRENT = 0;

const updateImportScanState = (
  state: ImportScanState,
  match: { readonly lineIdx: number; readonly matchedId: string; readonly targetId: string },
): void => {
  state.lastRulesImportIdx = match.lineIdx;
  if (
    naturalOrder(match.matchedId, match.targetId) > AFTER_CURRENT &&
    state.insertImportAt === NOT_FOUND
  ) {
    state.insertImportAt = match.lineIdx;
  }
};

const scanImportLines = (lines: readonly string[], id: string): ImportScanState => {
  const state: ImportScanState = { insertImportAt: NOT_FOUND, lastRulesImportIdx: NOT_FOUND };
  for (const [idx, line] of lines.entries()) {
    const match = RULES_IMPORT_RE.exec(line);
    if (match) {
      let ruleId = '';
      if (match.groups) {
        ruleId = match.groups.ruleId ?? '';
      }
      updateImportScanState(state, { lineIdx: idx, matchedId: ruleId, targetId: id });
    }
  }
  return state;
};

const findImportInsertionIndex = (lines: readonly string[], id: string): number => {
  const state = scanImportLines(lines, id);
  if (state.insertImportAt === NOT_FOUND) {
    if (state.lastRulesImportIdx === NOT_FOUND) {
      throw new Error('no ./rules/ imports found in builtin-rules.ts');
    }
    state.insertImportAt = state.lastRulesImportIdx + NEXT_LINE;
  }
  return state.insertImportAt;
};

const insertRegistryEntry = (lines: string[], id: string, camelId: string): void => {
  const registryStartIdx = findMarkerLine({
    lines,
    marker: RULE_REGISTRY_OPEN,
    what: 'RULE_REGISTRY opening',
  });
  const registryEndIdx = findMarkerLine({
    from: registryStartIdx + NEXT_LINE,
    lines,
    marker: RULE_REGISTRY_CLOSE,
    what: 'RULE_REGISTRY closing',
  });
  // Match the shorthand form used when the key and identifier coincide (e.g.
  // `provenance` is written without quotes); fall back to the explicit
  // `'<id>': <camelId>` form for every kebab-cased or otherwise non-matching
  // identifier. Keeps the registry visually identical to a hand-edit.
  let entry = `  '${id}': ${camelId},`;
  if (id === camelId) {
    entry = `  ${camelId},`;
  }
  lines.splice(registryEndIdx, INSERT_COUNT, entry);
};

export const insertBuiltinRuleEntries = (source: string, id: string, camelId: string): string => {
  const importLine = `import { ${camelId} } from './rules/${id}.ts';`;
  if (source.includes(importLine)) {
    throw new Error(`rule '${id}' already imported in builtin-rules.ts`);
  }

  const lines = source.split('\n');
  const insertImportAt = findImportInsertionIndex(lines, id);
  lines.splice(insertImportAt, INSERT_COUNT, importLine);

  insertRegistryEntry(lines, id, camelId);

  return lines.join('\n');
};
